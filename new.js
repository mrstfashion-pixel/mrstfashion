let productPrice = 0;

function openForm(name, price, img, desc){

    productPrice = parseInt(price.replace(/[^\d]/g,""));

    document.getElementById("formPopup").style.display="flex";

    document.getElementById("previewImg").src = img;
    document.getElementById("productName").innerText = name;
    document.getElementById("productDetails").innerText = desc;
    document.getElementById("productPrice").innerText = productPrice;

    updateTotal();
}

function updateTotal(){

    let qty = Number(document.getElementById("quantity").value);

    let delivery = Number(document.getElementById("delivery").value);

    let total = (productPrice * qty) + delivery;

    document.getElementById("totalPrice").value = "৳ " + total;
}

document.getElementById("quantity").addEventListener("input", updateTotal);

document.getElementById("delivery").addEventListener("change", updateTotal);
